# Função "print" exibi/imprime na tela
# Uma função possui um "argumento"
# Para utilizar mais de uma argumento, usamos a vírgula

print(12, 34, sep= "-") # Argumentos não nomeados
print(56, 78, sep= '/') # Por padrão, o print exibe na tela e insere um espaço entre os argumentos
print(9, 10, sep= '') # O Python é case sensitive
